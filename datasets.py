class MinkowskiMethod(object):
    r = 2
    method = 'minkowski'

    def __init__(self, r=2):
        self.r = r

    def serialize(self):
        return {
            'r': self.r,
            'method': self.method
        }


class ImplicationMethod(object):
    method = 'implication-similarity'
    aggregator = 'average'
    implication = 'lukasiewicz'
    tnorm = 'lukasiewicz'

    def __init__(self, aggregator='average', implication='lukasiewicz', tnorm='lukasiewicz'):
        self.aggregator = aggregator
        self.implication = implication
        self.tnorm = tnorm

    def serialize(self):
        return {
            'method': self.method,
            'aggregator': self.aggregator,
            'implication': self.implication,
            'tnorm': self.tnorm
        }


class SimplifiedJaccardIndex(object):
    method = constants.SIMPLIFIED_JACCARD_INDEX
    aggregator = 'average'
    implication = 'lukasiewicz'
    tnorm = 'lukasiewicz'

    def __init__(self, aggregator='average', tkonorm=constants.TKONORM_MAXIMUM, tnorm=constants.TNORM_MINIMUM):
        self.aggregator = aggregator
        self.tkonorm = tkonorm
        self.tnorm = tnorm

    def serialize(self):
        return {
            'method': constants.SIMPLIFIED_JACCARD_INDEX,
            params.AGGREGATOR: self.aggregator,
            params.TKNORM: self.tkonorm,
            params.TNORM: self.tnorm
        }


class AngularDistanceMethod(object):
    def serialize(self):
        return {
            'method': 'angular-distance'
        }


class JaccardIndexMethod(object):
    def __init__(self, alpha=1, beta=1, gamma=0, evaluator='average'):
        self.alpha = alpha
        self.beta = beta
        self.gamma = gamma
        self.evaluator = evaluator

    def serialize(self):
        return {
            'method': 'jaccard-index',
            'alpha': self.alpha,
            'beta': self.beta,
            'gamma': self.gamma,
            'evaluator': self.evaluator,
        }


class RawSets(object):
    setA = ""
    setB = ""

    def __init__(self, setA, setB):
        self.setA = setA
        self.setB = setB

    def serialize(self):
        return {
            'setA': self.setA,
            'setB': self.setB
        }


class DataSet(object):
    def toKeyLabelMap(self):
        return {
            'key': self.key,
            'label': self.label
        }

    def serialize(self):
        return {
            'label': self.label,
            'key': self.key,
            'description': self.description,
            'rawSets': self.rawSets.serialize(),
            'methods': map(lambda method: method.serialize(), self.methods)
        }

    def __init__(self, label, description):
        self.key = uuid.uuid4().hex
        self.label = label
        self.description = description
        self.rawSets = RawSets('', '')
        self.methods = []

    def withSetA(self, setA):
        self.rawSets.setA = setA
        return self

    def withSetB(self, setB):
        self.rawSets.setB = setB
        return self

    def withSameSets(self, fuzzySet):
        self.rawSets.setA = fuzzySet
        self.rawSets.setB = fuzzySet
        return self

    def withMethods(self, methods):
        self.methods = methods
        return self


